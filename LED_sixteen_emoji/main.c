#include "reg52.h"
#include "delay.h"
#include "sixteen_hc595.h"

u8 col[]={0x30,0x0C,0x20,0x04,0xF8,0x1F,0x04,0x20,0x12,0x44,0x29,0x8A,0x45,0x91,0x01,0x80,
0x01,0x80,0x45,0xA1,0x09,0x90,0xF1,0x8F,0x82,0x41,0x04,0x20,0xF8,0x1F,0x00,0x00};//横向扫描

u8 row[]={0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};

void main()
{
	u8 i =0;
	while(1)
	{
		for(i=0;i<16;i++)
		{
			hc595_write_data(row[i],row[i+24],~col[i*2],~col[i*2+1]);
			delay(10);//稳定显示
			hc595_write_data(0x00,0x00,0x00,0x00);//消影
		}
	}
}