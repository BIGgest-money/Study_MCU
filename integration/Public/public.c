#include "public.h"



/****************************************************************************
* 函 数 名		：delay_10us
* 函 数 功 能	：延时函数，xus=1时，大约延时10us
* 输 入				：xus
* 输 出				：无
*****************************************************************************/

void delay_10us(u16 xus)
{
	while(xus--);
}


/****************************************************************************
* 函 数 名		：delay_ms
* 函 数 功 能	：延时函数，xms=1时，大约延时1ms
* 输 入				：xms
* 输 出				：无
*****************************************************************************/


void delay_ms(u16 xms)
{
	u16 i,j;
	for(i=xms;i>0;i--)
		for(j=110;j>0;j--);
}


